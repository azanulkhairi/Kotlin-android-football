package com.dicoding.azanul.footballapps.api

import android.net.Uri
import com.dicoding.azanul.footballapps.BuildConfig

object TheSportDBApi {

    fun getEventDetail( idEvent :String?):String{
        return Uri.parse(BuildConfig.BASE_URL).buildUpon()
                .appendPath("api")
                .appendPath("v1")
                .appendPath("json")
                .appendPath(BuildConfig.TSDB_API_KEY)
                .appendPath("lookupevent.php")
                .appendQueryParameter("id",idEvent)
                .build().toString()
    }

    fun getEventsNext(idLeague : String?):String{
